#include "libft.h"
#include <stdio.h>
#include <string.h>

int	ft_atoi(const char *str)
{
	int			i;
	long long	res;
	int			sign;

	i = 0;
	res = 0;
	sign = 1;
	while ((str[i] >= 0x09 && str[i] <= 0x0D) || str[i] == 0x20)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] && ft_isdigit(str[i]))
	{
		res = res * 10 + (str[i] - '0');
		if (res > INT_MAX && sign == 1)
			return ((int) LONG_MAX);
		if (res < INT_MIN && sign == -1)
			return ((int) LONG_MIN);
		i++;
	}
	return ((int)(res * sign));
}

